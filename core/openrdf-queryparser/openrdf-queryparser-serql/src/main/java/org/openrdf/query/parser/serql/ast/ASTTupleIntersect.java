package org.openrdf.query.parser.serql.ast;

public class ASTTupleIntersect extends ASTTupleQuerySet {

	public ASTTupleIntersect(int id) {
		super(id);
	}

	public ASTTupleIntersect(SyntaxTreeBuilder p, int id) {
		super(p, id);
	}

	/** Accept the visitor. */
	public Object jjtAccept(SyntaxTreeBuilderVisitor visitor, Object data)
		throws VisitorException
	{
		return visitor.visit(this, data);
	}
}
